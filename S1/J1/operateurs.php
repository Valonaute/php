<?php
// -------------- Les opérateurs arithmétiques -------------------------

$somme = 100 ;
// Augmenter la valeure de la somme de 2 

$somme = $somme + 2 ;

echo $somme ;

$somme += 2 ;

echo $somme ;

// Reduire la valeure de la somme de 3 
$somme = $somme - 3 ; 
$somme -= 3 ;

echo $somme ;

// Augmenter la valeure d'une unité / Incrémenter 

$somme++ ;

echo $somme;

// Réduire d'une unité / Décrémenter 

$somme-- ;

echo $somme; 

// 